package calc2;

public class calc2 {

	
	public double valortotalsoma(double valor1, double valor2) {
		return valor1 + valor2;
	}
	
	public double valortotalsub(double valor1, double valor2) {
		return valor1 - valor2;
	}
	
	public double valortotalmulti(double valor1, double valor2) {
		return valor1 * valor2;
	}
	
	public double valortotaldiv(double valor1, double valor2) {
		return valor1 / valor2;
	}
	
	public double valortotalpoten(double valor1 , double valor2) {
		return Math.pow(valor1, valor2);
	}
	
	public double valortotalporcent(double valor1, double valor2) {
		 return (valor1/valor2)*100;   
	}
	
	public double valortotalraizquad(double valor1) {
		return Math.sqrt(valor1);
		
	}
	
	
}
